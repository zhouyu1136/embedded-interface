#include "key.h"

/*********************************************************************************************
* 名称：key_init()
* 功能：按键初始化
* 参数：无
* 返回：无
* 修改：
* 注释：
*********************************************************************************************/
void key_init(void)
{
    P2SEL &= ~(1<<0);                                               //配置按键检测管脚为通用IO
    P2DIR &= ~(1<<0);                                               //配置按键检测管脚为通输入模式
}

/*********************************************************************************************
* 名称：key_status()
* 功能：获取按键的状态
* 参数：按键号，在key.h头文件中宏定义为KEY1，KEY2
* 返回：K1和K2的状态，0/DOWN表示按下，1/UP表示没按下，返回-1表示参数错误
* 修改：
* 注释：参数只能填入K1或者K2,否则会返回-1
*********************************************************************************************/
signed char key_status()
{
    return K4;
}